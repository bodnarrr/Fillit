int		ft_atoi(const char *str)
{
	unsigned long int	n;
	int					sign;

	n = 0;
	sign = 1;
	while ((*str >= 9 && *str <= 13) || *str == ' ')
		str++;
	if (*str == '-')
	{
		sign = -1;
		str++;
	}
	else if (*str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		if ((n > 922337203685477580 || (n == 922337203685477580 \
						&& (*str - '0') > 7)) && sign == 1)
			return (-1);
		if ((n > 922337203685477580 || (n == 922337203685477580 \
						&& (*str - '0') > 8)) && sign == -1)
			return (0);
		n = n * 10 + (unsigned)(*str++ - '0');
	}
	return ((int)n * sign);
}
