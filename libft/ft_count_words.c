#include <string.h>

size_t	ft_count_words(char const *s, char c)
{
	size_t i;

	if (s == NULL || c == '\0')
		return (0);
	i = 0;
	while (*s != '\0')
	{
		while (*s == c)
			s++;
		if (*s != '\0')
			i++;
		while (*s != c && *s != '\0')
			s++;
	}
	return (i);
}
